let nums = [1, 2, 3, 1];

function containsDuplicate(arr) {
  for (let i = 0; i < arr.length; i++) {
    for (let v = i + 1; v < arr.length; v++) {
      if (arr[i] === arr[v]) return true;
    }
  }
  return false;
}

console.log(containsDuplicate(nums));

